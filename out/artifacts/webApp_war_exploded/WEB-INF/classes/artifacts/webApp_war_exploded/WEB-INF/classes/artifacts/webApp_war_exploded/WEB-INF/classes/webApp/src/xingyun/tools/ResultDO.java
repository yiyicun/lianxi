package xingyun.tools;

public class ResultDO {
	
	private int dxSum;
	private int oeSum;
	private int sum;
	private int low;
	private int high;
	
	public int getDxSum() {
		return dxSum;
	}
	public void setDxSum(int dxSum) {
		this.dxSum = dxSum;
	}
	public int getOeSum() {
		return oeSum;
	}
	public void setOeSum(int oeSum) {
		this.oeSum = oeSum;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	public int getLow() {
		return low;
	}
	public void setLow(int low) {
		this.low = low;
	}
	public int getHigh() {
		return high;
	}
	public void setHigh(int high) {
		this.high = high;
	}
	
	
	
}
